#!/usr/bin/env node
/**
 * Ingest a Phenol-Explorer content export into src/data/polyphenols.json (total
 * polyphenols mg/100g per USDA fdcId), the side table the FII reads (src/core/fii.ts).
 *
 * Phenol-Explorer (http://phenol-explorer.eu) is registration-gated and exports per
 * food × compound rows, with no USDA id — so the join is name→fdcId via a crosswalk you
 * maintain (a few hundred foods; name-matching by hand beats a fuzzy join you can't
 * audit). This script sums each food's compound contents to a total, joins through the
 * crosswalk, and reports the foods it could not place so coverage stays honest.
 *
 * Usage:
 *   node scripts/build-phenol-explorer.mjs <export.csv> <crosswalk.json> [out.json]
 *
 *   export.csv      Phenol-Explorer content export. Needs a food-name column (matched
 *                   case-insensitively against /food/) and a numeric content column
 *                   (matched against /content|mean|mg/); one row per food×compound, in
 *                   mg/100 g fresh weight. Rows are summed per food.
 *   crosswalk.json  { "<Phenol-Explorer food name>": <fdcId>, ... }
 *   out.json        defaults to src/data/polyphenols.json
 *
 * Re-run after updating either input; it overwrites the seed wholesale.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join, resolve } from 'node:path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
/** User-supplied CLI paths resolve against the caller's CWD; defaults against repo root. */
const fromCwd = (p) => resolve(process.cwd(), p);
const log = (...a) => process.stderr.write(a.join(' ') + '\n');
const die = (m) => { log('✗ ' + m); process.exit(1); };

const [csvPath, crosswalkPath, outArg] = process.argv.slice(2);
if (!csvPath || !crosswalkPath) {
  die('usage: node scripts/build-phenol-explorer.mjs <export.csv> <crosswalk.json> [out.json]');
}
const out = outArg ?? 'src/data/polyphenols.json';

/** Minimal RFC-4180-ish CSV parse (handles quoted fields with commas and "" escapes). */
function parseCSV(text) {
  const rows = [];
  let row = [], field = '', inQuotes = false;
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (inQuotes) {
      if (c === '"') { if (text[i + 1] === '"') { field += '"'; i++; } else inQuotes = false; }
      else field += c;
    } else if (c === '"') inQuotes = true;
    else if (c === ',') { row.push(field); field = ''; }
    else if (c === '\n' || c === '\r') {
      if (c === '\r' && text[i + 1] === '\n') i++;
      if (field !== '' || row.length) { row.push(field); rows.push(row); row = []; field = ''; }
    } else field += c;
  }
  if (field !== '' || row.length) { row.push(field); rows.push(row); }
  return rows;
}

log(`→ reading ${csvPath}…`);
const rows = parseCSV(readFileSync(fromCwd(csvPath), 'utf8'));
if (rows.length < 2) die('CSV has no data rows');
const header = rows[0].map((h) => h.trim());
const foodCol = header.findIndex((h) => /food/i.test(h));
const contentCol = header.findIndex((h) => /content|mean|mg/i.test(h));
if (foodCol < 0) die(`no food-name column (looked for /food/) in: ${header.join(', ')}`);
if (contentCol < 0) die(`no content column (looked for /content|mean|mg/) in: ${header.join(', ')}`);
log(`  food column "${header[foodCol]}", content column "${header[contentCol]}"`);

const crosswalk = JSON.parse(readFileSync(fromCwd(crosswalkPath), 'utf8'));

log('→ summing compound contents per food…');
const totals = new Map(); // food name → summed mg/100g
for (const r of rows.slice(1)) {
  const food = (r[foodCol] ?? '').trim();
  const val = parseFloat(r[contentCol]);
  if (!food || !Number.isFinite(val)) continue;
  totals.set(food, (totals.get(food) ?? 0) + val);
}
log(`  ${totals.size} distinct foods in export`);

const result = {
  _doc: 'Total polyphenol content (mg/100g) per USDA fdcId, merged into NutrientVector.polyphenol_mg and read by the FII (src/core/fii.ts). GENERATED by scripts/build-phenol-explorer.mjs from a Phenol-Explorer export — re-run to refresh.',
  _source: `Phenol-Explorer content export, joined to USDA fdcId via ${crosswalkPath}.`,
};
let placed = 0;
const unmatched = [];
for (const [food, total] of totals) {
  const fdcId = crosswalk[food];
  if (fdcId == null) { unmatched.push(food); continue; }
  result[String(fdcId)] = {
    polyphenol_mg: Math.round(total * 10) / 10,
    source: `Phenol-Explorer (${food})`,
    confidence: 'high',
  };
  placed++;
}

writeFileSync(outArg ? fromCwd(out) : join(ROOT, out), JSON.stringify(result, null, 2) + '\n');
log(`✓ wrote ${out}: ${placed} foods placed, ${unmatched.length} unmatched`);
if (unmatched.length) {
  log('  unmatched (add to the crosswalk to include):');
  for (const f of unmatched.slice(0, 20)) log(`    - ${f}`);
  if (unmatched.length > 20) log(`    … and ${unmatched.length - 20} more`);
}
